package personmobile;

public class person {
	private int pid;
	private String pname;
	private int page;
	private aadhar adh;
	private mobile[] mob;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public aadhar getAdh() {
		return adh;
	}
	public void setAdh(aadhar adh) {
		this.adh = adh;
	}
	public mobile[] getMob() {
		return mob;
	}
	public void setMob(mobile[] mob) {
		this.mob = mob;
	}
	

}
