package flowcontro;

public class switchc {

	public static void main(String[] args) {
		int a=8;
		switch(a)
		{
		case 1:
			System.out.println("number is 3");
			break;
		case 2:
			System.out.println("number is 2");
			break;
		case 7:
			System.out.println("number is 7");
			break;
			default:
				System.out.println("no match found");
		}

	}

}
