package flowcontro;

public class whileloop {

	public static void main(String[] args) {
		int i=1;
		while(i<=12)
		{
			System.out.println("hours================"+i);
			i++;
			int j=1;
			while(j<=60)
			{
				System.out.println("minutes=========="+j);
				j++;
				int k=1;
				while(k<=60)
				{
					System.out.println("seconds====="+k);
					k++;
				}

			}

	
		}


	}
}


