package loops;

public class reverse {

	public static void main(String[] args) {
		char ch;
		System.out.println("reverse upper case letters");
		for(ch=90;ch>=65;ch--)
		{
			System.out.println(ch);
		}
		System.out.println("reverse lower case letters");
		for(ch=122;ch>=97;ch--)
		{
			System.out.println(ch);
		}

	}

}
